package com.Dinesh.csc106.prac14.travel;

public abstract class Other_vehicle extends Vehicle {
	
	private String other_vehicle_name;
	private String other_vehicle_role;
	
	
	public String getOther_vehicle_name() {
		return other_vehicle_name;
	}
	public void setOther_vehicle_name(String other_vehicle_name) {
		this.other_vehicle_name = other_vehicle_name;
	}
	public String getOther_vehicle_role() {
		return other_vehicle_role;
	}
	public void setOther_vehicle_role(String other_vehicle_role) {
		this.other_vehicle_role = other_vehicle_role;
	}
	
	

}
