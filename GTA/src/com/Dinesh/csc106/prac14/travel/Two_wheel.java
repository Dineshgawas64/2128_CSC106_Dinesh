package com.Dinesh.csc106.prac14.travel;

public abstract class Two_wheel extends Vehicle {
	
	private String two_wheeler_name;
	private String two_wheeler_role;
	
	
	public String getTwo_wheeler_name() {
		return two_wheeler_name;
	}
	public void setTwo_wheeler_name(String two_wheeler_name) {
		this.two_wheeler_name = two_wheeler_name;
	}
	public String getTwo_wheeler_role() {
		return two_wheeler_role;
	}
	public void setTwo_wheeler_role(String two_wheeler_role) {
		this.two_wheeler_role = two_wheeler_role;
	}
	
	
	

}
